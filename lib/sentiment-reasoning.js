export function sentimentLabel(score) {
  if (score == null || Number.isNaN(Number(score))) return "Unknown";
  const value = Number(score);
  return value > 0.25 ? "Positive" : value < -0.25 ? "Negative" : "Neutral";
}

export function magnitudeLabel(magnitude) {
  const value = Number(magnitude);
  if (!Number.isFinite(value)) return "unknown strength";
  if (value < 0.5) return "low emotional strength";
  if (value < 2) return "moderate emotional strength";
  if (value < 5) return "strong emotional strength";
  return "very strong emotional content";
}

function compactSentence(value, max = 180) {
  const text = String(value || "").replace(/\s+/g, " ").trim();
  if (text.length <= max) return text;
  return `${text.slice(0, max - 1).trim()}…`;
}

export function buildSentimentReason(sentiment, sentences = []) {
  if (!sentiment || sentiment.score == null || sentiment.magnitude == null) {
    return {
      summary: "Sentiment reasoning is unavailable because Google NLP score or magnitude is missing.",
      score_interpretation: "No score available.",
      magnitude_interpretation: "No magnitude available.",
      evidence: [],
      method: "rule_based_interpretation",
      disclaimer: "Google NLP returns score and magnitude; this explanation is generated transparently from those values."
    };
  }

  const score = Number(sentiment.score);
  const magnitude = Number(sentiment.magnitude);
  const label = sentimentLabel(score);
  const strength = magnitudeLabel(magnitude);

  let scoreInterpretation;
  let summary;

  if (label === "Positive") {
    scoreInterpretation = `The score ${score.toFixed(3)} is above +0.25, so the overall emotional leaning is positive.`;
    summary = `Positive overall: the score is above the dashboard’s +0.25 threshold, while magnitude ${magnitude.toFixed(3)} indicates ${strength}.`;
  } else if (label === "Negative") {
    scoreInterpretation = `The score ${score.toFixed(3)} is below -0.25, so the overall emotional leaning is negative.`;
    summary = `Negative overall: the score is below the dashboard’s -0.25 threshold, while magnitude ${magnitude.toFixed(3)} indicates ${strength}.`;
  } else if (magnitude >= 2) {
    scoreInterpretation = `The score ${score.toFixed(3)} sits between -0.25 and +0.25, so the document is neutral overall.`;
    summary = `Neutral but emotionally active: positive, negative or strongly framed sentences balance near zero, while magnitude ${magnitude.toFixed(3)} shows ${strength}.`;
  } else {
    scoreInterpretation = `The score ${score.toFixed(3)} sits between -0.25 and +0.25, so the document is neutral overall.`;
    summary = `Mostly factual or mildly framed: the score remains near zero and magnitude ${magnitude.toFixed(3)} indicates ${strength}.`;
  }

  const evidence = (Array.isArray(sentences) ? sentences : [])
    .filter(item => item && item.text && item.score != null)
    .map(item => ({
      text: compactSentence(item.text),
      score: Number(item.score),
      magnitude: Number(item.magnitude || 0),
      label: sentimentLabel(item.score),
      importance: Math.abs(Number(item.score)) * Math.max(Number(item.magnitude || 0), 0.1)
    }))
    .sort((a, b) => b.importance - a.importance)
    .slice(0, 3)
    .map(({ importance, ...item }) => item);

  return {
    label,
    summary,
    score_interpretation: scoreInterpretation,
    magnitude_interpretation: `Magnitude ${magnitude.toFixed(3)} reflects ${strength}. Magnitude is not normalized for article length, so longer articles can naturally have larger values.`,
    evidence,
    method: "rule_based_interpretation",
    disclaimer: "Google NLP supplies score, magnitude and sentence-level sentiment. The written explanation is generated by transparent rules; it is not a separate opinion from Google."
  };
}
